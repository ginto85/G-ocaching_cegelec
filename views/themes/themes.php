<article class="naturalsAreasTheme">
    <h3>Préserver les milieux naturels</h3>
    <div><img src="./assets/img/milieux.png" alt="Logo préserver les milieux naturels"></div>
    <p>Surexploitation des ressources, dérèglement climatique et pollution... les milieux naturels <strong>(eau, air, sols, biodiversité) sont en danger !</strong> 
        Ils sont bien souvent irremplaçables et leur modification ou destruction peut entraîner des conséquences irréversibles.<br/>
        Et cela se traduit par des actions sur le terrain.</p>
    <p>Nous installons des boucles fermées de recyclage pour <em>optimiser la consommation d’eau sur les chantiers</em>.</p>
    <p>Nous limitons l’emploi de produits phytosanitaires dans les traitements des sols. 
        Plusieurs de nos filiales sont également spécialisées dans les travaux de <em>renaturation</em>, 
        qui améliorent la qualité écologique d’un milieu : <em>restauration de cours d’eau</em>, <em>création de mares</em>, etc.</p>
    <p>Nous sommes désormais engagés dans la démarche volontaire <strong>act4nature international</strong>, 
        visant à intégrer le sujet de la biodiversité dans toutes les activités.</p>
</article>
