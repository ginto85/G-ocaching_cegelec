    <h3>Agir pour le climat</h3>
    <div><img src="./assets/img/climat.png" alt="Logo d'Agir pour le climat"></div>
    <p>La planète se réchauffe et les conséquences se ressentent déjà ! 
        La communauté scientifique s'alarme et invite chacun d'entre nous à se mobiliser contre le réchauffement climatique.</p>
    <p>Objectif : <strong>limiter la hausse des températures à 2 °C</strong> d’ici la fin du siècle. Comment pouvons-nous y contribuer ? 
        Nous nous étions engagés à réduire l'intensité carbone de nos activités de 30 % entre 2009 et 2020.</p>

    <p>Nous souhaitons poursuivre cette dynamique en nous engageant à <strong>réduire de 40 % nos émissions directes de CO2</strong> d'ici 2030.</p>
    <p>Pour y parvenir, nous continuons de contrôler:</p>
    <ul>
        <li><p>&rarr;<strong>Nos émissions directes, liées à la consommation énergétique</strong> :</p></li>
        <li>
            <ul>
                <li><p>&rarr;<em>des véhicules,</em></p></li>
                <li><p>&rarr;<em>des engins,</em></p></li>
                <li><p>&rarr;<em>des batiments.</em></p></li>
            </ul>
        </li>
        <li><p>&rarr;<strong>Nos émissions indirectes, issues des consommations de nos fournisseurs et de nos clients</strong>.</p></li>
    </ul>
    <p></p>
</article>
