<article class="optimisationTheme">
    <h3>Optimiser les ressources grâce à l'économie circulaire</h3>
    <div><img src="./assets/img/eco-circulaire.png" alt="logo d'une économie circulaire"></div>
    <p>Les ressources de la planète s’amenuisent ! 
        Il est urgent d’enrayer la raréfaction des ressources naturelles dont nos activités dépendent. Comment y arriver ? 
        En adaptant une démarche d'éco-conception, une approche systémique et territoriale et des méthodes inclusives
         et collaboratives.</p>
    <p>Pour cela, nous nous concentrons sur trois objectifs :</p>
    <ul>
        <li>
            <p>&rarr;<em>améliorer le tri</em> pour systématiser la <em>valorisation des déchets</em> ;</p>
        </li>
        <li>
            <p>&rarr;favoriser les techniques et <em>matériaux sobres en ressources naturelles</em> ;</p>
        </li>
        <li>
            <p>&rarr;<em>limiter l'extraction de matériaux primaires</em> en favorisant l’<em>emploi de matériaux recyclés</em>.</p>
        </li>
    </ul>
    <p>Ses enjeux de mise en œuvre passent notamment par des <strong>partages de bonnes pratiques</strong> 
        et de la mise en place de projets transverses et communs.</p>
    <p>Pour cela, nous nous basons sur les trois étapes de l’économie circulaire :</p>
    <ul>
        <li><p>&rarr;<em>Réutilisation</em></p></li>
        <li><p>&rarr;<em>Réemploi</em></p></li>
        <li><p>&rarr;<em>Recyclage.</em></p></li>
    </ul>
    <p><strong>Et n’oubliez pas, le meilleur déchet, c’est celui que l’on ne créé pas!</strong></p>
    <p><strong>Limiter la consommation des ressources naturelles.</strong></p>
</article>
